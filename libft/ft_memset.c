#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*ptr;
	size_t	i;

	ptr = (unsigned char *) b;
	i = 0;
	while (len > 0)
	{
		ptr[i] = (unsigned char) c;
		i++;
		len--;
	}
	return (b);
}
